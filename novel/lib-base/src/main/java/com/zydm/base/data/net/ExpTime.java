public class ExpTime {

    public static final int ZERO = 0;
    public static final int ONE_SECOND = 1;
    public static final int ONE_MINUTE = 60;
    public static final int TWO_MINUTE = ONE_MINUTE * 2;
    public static final int FIVE_MINUTE = ONE_MINUTE * 5;
    public static final int TEN_MINUTE = ONE_MINUTE * 10;
    public static final int HALF_HOUR = TEN_MINUTE * 3;
    public static final int ONE_HOUR = TEN_MINUTE * 6;
    public static final int HALF_DAY = ONE_HOUR * 12;
    public static final int ONE_DAY = ONE_HOUR * 24;
    public static final int THREE_DAY = ONE_HOUR * 24 * 3;
    public static final int ONE_WEEK = ONE_DAY * 7;
}
